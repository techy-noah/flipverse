function generateQuestions() {
  const result = [];
  const prefixCounters = {};

  for (const [deckId, qs] of Object.entries(questionSets)) {
    let counter = 0;
    for (const [question, answer, reference, difficulty] of qs) {
      counter++;
      // Generate a simple prefix based on deck
      let prefix;
      switch (deckId) {
        case 'deck-old-testament': prefix = 'q-ot-'; break;
        case 'deck-jesus': prefix = 'q-jesus-'; break;
        case 'deck-parables': prefix = 'q-par-'; break;
        case 'deck-commandments': prefix = 'q-com-'; break;
        case 'deck-apostles': prefix = 'q-act-'; break;
        case 'deck-psalms': prefix = 'q-psa-'; break;
        default: prefix = 'q-';
      }
      result.push({
        id: `${prefix}${counter}`,
        deck_id: deckId,
        question,
        answer,
        reference,
        difficulty,
      });
    }
  }

  return result;
}

